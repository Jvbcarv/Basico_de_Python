#Variáveis float (ponto flutuante): Podem ser positivas (+) ou negativas (-)

pi = 3.14
numero_euler = 2.71
escala_terremoto = -4.55

print(pi)
print(numero_euler)
print(escala_terremoto)

#Strings e chars: Uma string é uma sequência de caracteres enquanto um char representa um único caractere.

letra = 'a'
